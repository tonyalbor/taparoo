import UIKit

extension UIColor {
    
    class func taparooRed() -> UIColor {
        return UIColor(red: 220/255, green: 0/255, blue: 20/255, alpha: 1)
    }
    
    class func taparooBlue() -> UIColor {
        return UIColor(red: 0, green: 225/255, blue: 225/255, alpha: 1)
    }
    
    class func taparooGreen() -> UIColor {
        return UIColor(red: 0, green: 255/255, blue: 100/255, alpha: 1)
    }
    
    class func taparooYellow() -> UIColor {
        return UIColor(red: 255/255, green: 240/255, blue: 100/255, alpha: 1)
    }
    
    class func taparooGreenBackground() -> UIColor {
        return UIColor(red: 120/255, green: 190/255, blue: 155/255, alpha: 1)
    }
}